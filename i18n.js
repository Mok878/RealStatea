let currentLang = localStorage.getItem('dardz-lang') || 'en';

function applyLang(lang) {
  currentLang = lang;
  localStorage.setItem('dardz-lang', lang);
  const t = translations[lang];
  const html = document.getElementById('htmlRoot');

  if (lang === 'ar') {
    html.setAttribute('dir', 'rtl');
    html.setAttribute('lang', 'ar');
    document.body.classList.add('rtl');
  } else {
    html.setAttribute('dir', 'ltr');
    html.setAttribute('lang', lang === 'fr' ? 'fr' : 'en');
    document.body.classList.remove('rtl');
  }

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (!t[key]) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = t[key];
    } else if (el.tagName === 'OPTION') {
      el.textContent = t[key];
    } else {
      el.innerHTML = t[key];
    }
  });

  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });

  const titles = {
    en: 'DarDZ - Find Your Dream Home',
    fr: 'DarDZ - Trouvez Votre Maison',
    ar: 'دارDZ - ابحث عن منزلك'
  };
  document.title = titles[lang] || titles.en;
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});

